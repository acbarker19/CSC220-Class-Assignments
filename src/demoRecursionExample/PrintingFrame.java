    private void showFibo(int num) {
        
        for (int i = 0; i < num; i++) {
            outputArea.append("  "+fibo(i));
            
            if ((i + 1) % 5 == 0) outputArea.append("\n");
        }
    }
    
     
    private int fibo(int num) {
        if (num <= 1)
        {
            return 1;
        }else
        { 
            return fibo(num-1) + fibo(num-2);
        }
    }
    
