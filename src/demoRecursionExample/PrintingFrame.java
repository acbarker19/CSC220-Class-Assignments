    private void count2(int num) {
        if (num <= 1)
        {
            outputArea.append("    "+num+"\n");
        }else
        {
            for (int i = 0; i < num; i++) 
            {
                outputArea.append("     ");
            }
            outputArea.append("  "+num+"\n");
            
            count2(num - 1);
        }
    }
    
    private void buttonThreeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonThreeActionPerformed
        System.out.println("button three");
        int theNum = 0;
        outputArea.setText("");
        try
        {
            theNum = Integer.parseInt(fieldThreeInput.getText());
        }catch (Exception ex){
            outputArea.append("\n  Sorry, '"+fieldThreeInput.getText()+"' could not be parsed as an integer.");
            outputArea.append("\n\n  Please enter an integer greater than 0 and less than 10000");
            
            outputArea.append("\n\n  For now we will use 5.");
            theNum = 5;
        }
        
        if (theNum <= 0 || theNum >= 10000)
        {
            outputArea.append("\n\n  Please enter an integer greater than 0 and less than 10000");
            
            outputArea.append("\n\n  For now we will use 5.");
            theNum = 5;
        }
        outputArea.append("\n\n\n   int total = summation("+theNum+");   \n\n");
        
        int total = summation(theNum);
        outputArea.append("          total is  "+total);
        
    }//GEN-LAST:event_buttonThreeActionPerformed

    
    private int summation(int num) {
        if (num <= 1)
        {
            return num;
        }else
        {
            return num + summation(num - 1);
        }
    }
    
